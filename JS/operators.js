//Arthenetic Operators
/* +,-,*,/,
** - exponentation,
% - modulus (div rem),
++ - increment - increases by 1
-- - decrement - decreases by 1
*/

console.log(2+3)
console.log(3-2)
console.log(6*2)
console.log(66/6)

console.log(2**3)
var a=5
//post increment
console.log(a++)
console.log(a)
//preincrement
console.log(++a)
console.log(a)

console.log(6%4)

console.log(5**7)
console.log(99%5)

var b = 6
console.log(++b)
var c = 2 
console.log(--c)
